/**
 * Информация о пользователе от OAuth провайдера
 * Содержит основные данные пользователя и токены доступа
 */
export type UserInfoType = {
	/** Уникальный идентификатор пользователя */
	id: string
	/** Email адрес пользователя */
	email: string
	/** Полное имя пользователя */
	name: string
	/** URL аватара пользователя */
	picture: string
	/** Токен доступа (опционально) */
	access_token?: string | null
	/** Токен обновления (опционально) */
	refresh_token?: string
	/** Время истечения токена (опционально) */
	expires_at?: number
	/** Название провайдера аутентификации */
	provider: string
}
